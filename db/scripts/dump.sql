ALTER TABLE `AuctionDB`.`AUCTIONS` ADD INDEX `IS_ACTIVE` (`IS_ACTIVE`);
ALTER TABLE `AuctionDB`.`AUCTIONS` ADD INDEX `CREATED_AT` (`CREATED_AT`);

